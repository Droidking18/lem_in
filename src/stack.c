#include <stdlib.h>

void		push(int *stack, int *top, int nxt_room)
{
	(*top)++;
	stack[*top] = nxt_room;
}

void		pop(int *top)
{
	if (*top > -1)
		(*top)--;
	else
		exit(0);
}
